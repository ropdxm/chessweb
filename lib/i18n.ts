export function useI18n() {
  const { user } = useFirebaseUser();
  const profile = useUserProfile(user?.uid);
  const language: Language = profile?.pro && (profile.language === "kk" || profile.language === "ru" || profile.language === "fr") ? profile.language : "en";
  return useMemo(() => ({ language, t: translations[language] }), [language]);
}
